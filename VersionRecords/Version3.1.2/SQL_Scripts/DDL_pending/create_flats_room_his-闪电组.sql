/**创建公寓历史表**/
CREATE TABLE flat_flats_his (
  id INT(11) NOT NULL AUTO_INCREMENT COMMENT '公寓历史表ID',
  flatsId INT(11) NOT NULL COMMENT '公寓记录ID',
